<h1>Hilfe zum Bestellabschluss</h1>

<div>
Sie befinden sich unmittelbar vor dem Abschluss Ihrer Bestellung. Um diese 
abzuschlie&szlig;en sind nur noch wenige Eingaben n&ouml;tig, sodass wir Ihnen mit dieser 
Online Hilfe Hilfestellung geben m&ouml;chten, wenn Sie beim Ausf&uuml;llen doch auf 
Probleme sto&szlig;en sollten.
<br /><br />
<strong>1. Pflichtangaben</strong>

<ul>
  <li><strong>Versandart</strong></li>
  <li><strong>Zahlungsart</strong>
  </li>
</ul>
<br />
Um Ihre Bestellung an Sie ausliefern zu k&ouml;nnen, m&uuml;ssen Sie je eine Zahlungs- und 
Versandart ausw&auml;hlen. Eine Auswahl wird best&auml;tigt indem Sie auf den Button 
&quot;Speichern&quot; klicken. Eine nicht best&auml;tigte Auswahl erkennen Sie an dem 
blinkenden Speichern-Button. Jede Auswahl kann beliebig oft ver&auml;ndert werden, 
muss jedoch im Anschluss wieder best&auml;tigt werden. Auf Grund logistischer 
Umst&auml;nde kann nicht jede Zahlungsart mit jeder Versandart kombiniert werden. 
Wird eine solche Paarung ausgew&auml;hlt, werden Sie durch eine entsprechenden 
Hinweis darauf merksam gemacht und m&uuml;ssen daraufhin eine neue Auswahl treffen. 
Bei einzelnen Zahlungsarten (z.B. Nachnahme) werden evtl. zus&auml;tzliche Geb&uuml;hren 
berechnet. Diese Geb&uuml;hren werden Ihnen nach Auswahl einer Versandart automatisch 
angezeigt und der am Seitenende stehenden Gesamtsumme hinzugef&uuml;gt. Sie m&uuml;ssen in 
diesem Falle also keine manuelle Berechnung vornehmen, da jede Auswahl sofort 
den Gesamtpreis aktualisiert.
<br /><br />
<strong>2. &Auml;ndern der Liefer- oder Rechnungsanschrift</strong>
<br />
Sofern n&ouml;tig, k&ouml;nnen Sie bei Bedarf Ihre Liefer- oder/und Rechnungsanschrift 
&auml;ndern. Werden keine &Auml;nderungen vorgenommen, werden automatisch die bei der 
Kundenregistrierung angebenen Daten verwendet. Sollten die daf&uuml;r vorgesehenen 
Bereiche nicht sichtbar sein, klicken Sie dazu einfach auf die jeweilige 
&Uuml;berschrift, worauf Ihnen die Eingabemasken dynamisch eingeblendet werden. 
Sowohl bei der Auswahl der Rechnungs- als auch der Lieferanschrift stehen 
identische Auswahlm&ouml;glichkeiten zur Verf&uuml;gung, sodass Sie eine bei einer 
fr&uuml;heren Bestellung bereits angegebe Anschrift entweder nur ausw&auml;hlen oder eine 
neue Anschrift definieren. Ihre Eingabe best&auml;tigen Sie durch Anw&auml;hlen des 
Speichern-Button. M&ouml;chten Sie die Eingabe vorzeitig abbrechen, w&auml;hlen Sie dazu 
den Button zur&uuml;ck.
<br /><br />
Unter Umst&auml;nden nimmt eine andere oder neu definierte Lieferanschrift Einfluss 
auf die vorher getroffene Auswahl bei der Zahlungs- oder Versandart, sodass 
z.B. die Zahlungsart Nachnahme nicht au&szlig;erhalb von Deutschland m&ouml;glich ist, wenn 
diese Zahlungsart vorher ausgew&auml;hlt und z.B. Schweiz als neue Lieferanschrift 
gew&auml;hlt wurde. In diesem Fall wird die Auswahl der betroffenen Zahlungs- 
oder/und Versandart aufgehoben und Sie werden zur Neuauswahl aufgefordert.
<br /><br />
<strong>3. Bestellung pr&uuml;fen und &auml;ndern</strong>
<br />
Im mittleren Bereich k&ouml;nnen Sie nochmals Ihre Bestellung pr&uuml;fen und ggf. 
ab&auml;ndern. <br />
<span class="modal-help-products-decrease"></span> Artikelzahl reduzieren<br />
<span class="modal-help-products-increase"></span> Artikelzahl erh&ouml;hen<br />
<span class="modal-help-products-remove"></span> Artikel l&ouml;schen; ist 
nur 1 Artikel vorhanden, werden Sie nach der L&ouml;schung zum Warenkorb 
zur&uuml;ckgeleitet.

<br />
<strong>Attribute ver&auml;ndern:</strong> 
Sollte zu einem Artikel eine Variante ausgew&auml;hlt worden sein, kann hier nochmals 
die Variante ver&auml;ndert werden.<br />
Bei jeder &Auml;nderung, die Einfluss auf den Gesamtpreis nimmt, wird diese &Auml;nderung 
automatisch bei der Gesamtsumme Ihrer Bestellung neu berechnet und aktualisert.
<br /><br />
<strong>4. Ihre Anmerkungen</strong>
<br />
Wenn Sie uns zu Ihrer Bestellung eine Information zukommen lassen m&ouml;chten, dann 
tragen Sie Ihre Nachricht bitte in das daf&uuml;r vorgesehene Kommentarfeld ein. 
Hierzu stehen Ihnen insgesamt 256 Zeichen (inkl. Leerzeichen) zur Verf&uuml;gung.
<br /><br />
<strong>5. AGB und Widerrufsrecht</strong>
<br>
F&uuml;r Online Handel ist es notwendig, dass Sie unseren Allgemeinen 
Gesch&auml;ftsbedingungen und dem Widerrufsrecht zustimmen. Dies geschieht durch 
Anwahl der &quot;Check Boxen&quot; jeweils unterhalb der Textbereiche f&uuml;r AGB und 
Widerrufsrecht.
<br /><br />
<strong>5. Bestellung abschlie&szlig;en</strong>
<br />
Wenn Sie alle Eingaben vorgenommen haben, schlie&szlig;en Sie Ihre Bestellung &uuml;ber den 
Button &quot;Bestellen&quot; am Seitenende ab. Sie erhalten kurz darauf eine Best&auml;tigung 
Ihrer Bestellung per E-Mail zugeschickt. Pr&uuml;fen Sie deshalb unmittelbar nach 
Ihrer Bestellung Ihren E-Mail Posteingang. Mitunter werden E-Mails gelegentlich 
als &quot;SPAM&quot;-Mail identifiziert. Sie sollten dann im Bedarfsfall Ihr E-Mail 
Programm und den SPAM-Ordner des E-Mail Programms oder die entsprechenden Beeiche 
bei Ihrem E-Mail Provider auf Posteing&auml;nge &uuml;berpr&uuml;fen.
