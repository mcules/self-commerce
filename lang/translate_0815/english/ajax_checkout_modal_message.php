<h1>Help for your order</h1>

<div>You are immediately prior to complete your order.You are only a few steps away.<br />
  We hope we can help you with this short manual.<br />
  <br />
<strong>1. Required values</strong>

<ul>
  <li><strong>Shipping method</strong></li>
  <li><strong>Payment method</strong>
  </li>
</ul>
<p><br />
  To deliver you order you have to choose a payment module and a shipping module. A selection will be confirmed by clicking the &quot;Save&quot;-button. A unconfirmed selection is flagged by a blinking &quot;Save&quot;-Button. Each selection can be changed as often you want. Due to logistic circumstances not every payment method is compatible with every shipping method. If such a pairing is selected you will be warned by a short message. For some payment types (like COD) there are additional fees which will be automatically displayed next to the method and at the end of the page. So you don't have to make a manual calculation.<br />
  <br />
    <strong>2. Changing the shipping or billing address</strong>
    <br />
  If necessary, you can edit your delivery and / or billing address. The default value is the address you specified at the registration. If the designated areas are not shown, click on the relevant heading to make them visible. By clicking the &quot;Edit&quot;-Button you can dynamically change your address. Hit the &quot;Save&quot;-Button to store your address or hit the &quot;Cancel&quot;-Button to undo your inputs. <br />
  <br />
  It could be that an other or new delivery address take influence on  the selection made in advance to the payment or shipment.  In this case the selection of the  payment method and / or shipping method will be lost, and you are invited for reselection.<br />
  <br />
  <strong>3. Check and edit your order</strong><br />
  In the middle area you can once again check your order and if necessary amend.<br />
  <span class="modal-help-products-decrease"></span> Reduce number of products<br />
  <span class="modal-help-products-increase"></span> Increase number of products<br />
  <span class="modal-help-products-remove"></span> Remove a product; If only one product is available you will be redirected to the shopping cart.
  
  <br /><br />
  <strong>Edit attributes:</strong> You can edit the variants of the product<br />
  After every change which takes influence on the overall price, the order will be recalculated dynamically.
  <br />
  <br />
  <strong>4. Your comments</strong><br />
  If you want to tell us information about your order you can enter a short text into the text area.The text field is limited to 256 characters (including white spaces).<br />
  <br />
  <strong>5. Conditions and notices of revocation</strong><br>
  For online trading, it is necessary that you agree to our terms &amp; conditions and the notices of revocation. You can do this by selecting the &quot;check boxes&quot; below.<br />
  <br />
  <strong>5. Confirm order</strong>
  <br />
  If you have made all required entries you can close your order by hitting the &quot;Confirm Order&quot;-Button at the end of the page. You will receive confirmation by e-mail with a summary of your order.
  If you won't receive this e-mail please check your spam folders.</p>
